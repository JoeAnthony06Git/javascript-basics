
// Logical Operators in JavaScript

/*
Logical operators are used to combine multiple conditions or to invert a condition. 
They are commonly used in control flow statements like if, while, and for loops.
*/

// AND (&&) - Return true if both operands are true
console.log(true && true);   // true
console.log(true && false);  // false
console.log(false && true);  // false
console.log(false && false); // false

// OR (||) - Return true if at least one of the operands is true
console.log(true || true);   // true
console.log(true || false);  // true
console.log(false || true);  // true
console.log(false || false); // false

// NOT (!) - Return the opposite value of the operand
console.log(!true);  // false
console.log(!false); // true