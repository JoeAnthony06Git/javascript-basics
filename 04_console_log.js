
// console.log() is used to print messages to the console. It is commonly used for debugging purposes.

// Example usage:
console.log("Hello, World!"); // This will print "Hello, World!" to the console.

/* Type of console.log():
1. console.log() - Used to print general messages.
2. console.error() - Used to print error messages.
3. console.warn() - Used to print warning messages.
4. console.info() - Used to print informational messages.
5. console.debug() - Used to print debug messages. */

// Example of different console methods:
console.error("This is an error message.");
console.warn("This is a warning message.");
console.info("This is an informational message.");
console.debug("This is a debug message.");